package enums;

/**
 * Enumeration of arithmetic operations between two numbers.
 *
 * @author DorianBoel
 */
public enum ArithmeticOperationType {

	/**
	 * The type of an operation where a number is added to another.
	 */
	ADDITION,
	
	/**
	 * The type of an operation where a number is deducted to another.
	 */
	SUBSTRACTION,
	
	/**
	 * The type of an operation where a number is multiplied by another.
	 */
	MULTIPLICATION,
	
	/**
	 * The type of an operation where a number is divided by another.
	 */
	DIVISION
	
}
