package enums;

import entities.house.Room;

/**
 * Enumeration of existing types used to identify
 * the nature of a {@link Room}.
 * 
 * @author DorianBoel
 */
public enum RoomType {

	/**
	 * Represents a bathroom within a house.
	 */
	BATHROOM,
	
	/**
	 * Represents a bedroom within a house.
	 */
	BEDROOM,
	
	/**
	 * Represents a kitchen within a house.
	 */
	KITCHEN,
	
	/**
	 * Represents a living room, usually the primary room, within a house.
	 */
	LIVING_ROOM,
	
	/**
	 * Represents a water closet within a house.
	 */
	WATER_CLOSET;
	
}
